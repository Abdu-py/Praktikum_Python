# Operator aritmatika
x = 10
y = 3
print("Tambah:", x+y)
print("Bagi:", x/y)
print("Modulus:", x%y)
print("Kali:", x*y)
print("Floor:", x//y)

# Operator perbandingan
x = 10
y = 5
print(x == y)
print(x > y)
print(x < y)

# Operator logika
x = 10
y = 5
print((x>y) and (y>0))
print((x<y) or (y>0))
print(not(x>y))

# Operator bitwise
x = 5
y = 3
print(x & y)
print(x | y)
print(x ^ y)

# Operator penugasan
x = 5
x += 3
x *= 2
x /= 4
print(x)

# Operator identitas dan keanggotaan
x = [1,2,3]
y = x
print(x is y)
print(2 in x)